
public class Math {
	
	/**
	 * Sum the numbers
	 * @param a the first number
	 * @param b the second number
	 * @return the sum as integer
	 */
	public static  int sum(int a, int b) {
		return a+b;
	}
	
	/***
	 * Divide the numbers
	 * @param a the first number
	 * @param b the second number
	 * @return the result as integer
	 */
	public static int divide(int a, int b) {
		return (int) a/b;
	}

}
